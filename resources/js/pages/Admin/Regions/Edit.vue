<script setup lang="ts">
import AppLayout from '@/layouts/AppLayout.vue';
import { type BreadcrumbItem } from '@/types';
import type { PageProps as InertiaPageProps } from '@inertiajs/core';
import { Head, router, useForm, usePage } from '@inertiajs/vue3';

interface Region {
    hashid: string;
    name: string;
    active: boolean;
}

// Extend default Inertia PageProps to include the region to edit
interface PageProps extends InertiaPageProps {
    region: Region;
}

const page = usePage<PageProps>();
const region = page.props.region;

const title = 'Edit Region';
const breadcrumbs: BreadcrumbItem[] = [
    { title: 'Dashboard', href: '/admin/dashboard' },
    { title: 'Regions', href: '/admin/regions' },
    { title, href: '' },
];

// Form initialized with existing region data
const form = useForm({
    name: region.name,
    active: region.active,
});

// Submit handler for updating the region
function submitRegion() {
    form.put(`/admin/regions/${region.hashid}`, {
        onSuccess: () => {
            router.get('/admin/regions'); // redirect to regions index after successful update
        },
    });
}
</script>

<template>
    <Head :title="title" />

    <AppLayout :breadcrumbs="breadcrumbs">
        <div class="p-4">
            <div class="flex flex-col space-y-4 rounded-xl bg-white p-4 text-[color:var(--card-foreground)]">
                <div class="flex items-center justify-between">
                    <h4 class="text-2xl font-bold">{{ title }}</h4>
                    <router-link href="/admin/regions" class="text-sm text-[color:var(--primary)] hover:underline">‚Üê Back</router-link>
                </div>

                <hr class="my-1 border-[color:var(--border)]" />

                <form @submit.prevent="submitRegion" class="mt-2 space-y-4 px-4">
                    <!-- Region Name -->
                    <div>
                        <input
                            v-model="form.name"
                            id="name"
                            type="text"
                            required
                            placeholder="Enter region name"
                            class="w-full rounded border border-[color:var(--border)] px-3 py-2 focus:ring-2 focus:ring-[color:var(--primary)] focus:outline-none"
                        />
                        <div v-if="form.errors.name" class="mt-1 text-sm text-red-600">{{ form.errors.name }}</div>
                    </div>

                    <!-- Active Checkbox -->
                    <div class="flex items-center space-x-2">
                        <input
                            id="active"
                            type="checkbox"
                            v-model="form.active"
                            class="h-4 w-4 rounded border border-gray-300 text-primary focus:ring-primary"
                        />
                        <label for="active" class="select-none">Active</label>
                    </div>

                    <button
                        type="submit"
                        :disabled="form.processing"
                        class="rounded bg-[color:var(--primary)] px-4 py-2 text-white transition-colors duration-200 hover:bg-[color:var(--secondary)] hover:text-[color:var(--secondary-foreground)]"
                    >
                        {{ form.processing ? 'Updating...' : 'Update' }}
                    </button>
                </form>
            </div>
        </div>
    </AppLayout>
</template>
